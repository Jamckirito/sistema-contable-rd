/** Genera RNC ficticio (9 dígitos) */
export function rncFake(seed: number): string {
  const n = 100000000 + (Math.abs(seed) % 900000000);
  return n.toString();
}

/** Genera cédula ficticia (11 dígitos) */
export function cedulaFake(seed: number): string {
  const n = 10000000000 + (Math.abs(seed) % 90000000000);
  return n.toString();
}

/** Entero aleatorio en [min, max] */
export function randInt(min: number, max: number): number {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

/** Elemento aleatorio de un array */
export function pick<T>(arr: T[], seed?: number): T {
  const i = seed !== undefined ? Math.abs(seed) % arr.length : Math.floor(Math.random() * arr.length);
  return arr[i];
}

/** N elementos aleatorios sin repetir (por índice) */
export function pickMany<T>(arr: T[], count: number, seed: number): T[] {
  const indices = new Set<number>();
  while (indices.size < Math.min(count, arr.length)) {
    indices.add((seed + indices.size * 31) % arr.length);
  }
  return [...indices].map((i) => arr[i]);
}
